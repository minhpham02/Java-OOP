public class SV {
    private String id, name, num, mail;

    public SV(String id, String name, String num, String mail) {
        this.id = id;
        this.name = name;
        this.num = num;
        this.mail = mail;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getNum() {
        return num;
    }

    public String getMail() {
        return mail;
    }
            
}
